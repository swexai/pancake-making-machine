{
    if (pvParameters);  /* Unused parameter */
    
    const TickType_t xDelay = pdMS_TO_TICKS(1);  /* 1 ms = 1 kHz */
    
    for (;;) {
        /* Safety is always checked first */
        safety_monitor();
        control_system_update();
        
        vTaskDelay(xDelay);
    }
}

